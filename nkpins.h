/*
Microbit pins - for my sanity

The default mbed Pxxx definitions do not align with those of the micro:bit - so these definitions add clarity...
*/

#define mbit_p0     P0_3        //P0 is the left most pad (ANALOG/DIGITAL) used to be P0_3 on green board
#define mbit_p1     P0_2        //P1 is the middle pad (ANALOG/DIGITAL)
#define mbit_p2     P0_1        //P2 is the right most pad (ANALOG/DIGITAL) used to be P0_1 on green board
#define mbit_p3     P0_4        //COL1 (ANALOG/DIGITAL)
#define mbit_p4     P0_5        //COL2 (ANALOG/DIGITAL)
#define mbit_p5     P0_17       //BTN_A
#define mbit_p6     P0_12       //COL9
#define mbit_p7     P0_11       //COL8
#define mbit_p8     P0_18       //PIN 18
#define mbit_p9     P0_10       //COL7
#define mbit_p10    P0_6        //COL3 (ANALOG/DIGITAL)
#define mbit_p11    P0_26       //BTN_B
#define mbit_p12    P0_20       //PIN 20
#define mbit_p13    P0_23       //SCK
#define mbit_p14    P0_22       //MISO
#define mbit_p15    P0_21       //MOSI
#define mbit_p16    P0_16       //PIN 16
#define mbit_p19    P0_0        //SCL
#define mbit_p20    P0_30       //SDA
